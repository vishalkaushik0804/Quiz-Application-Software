    private void btnregActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregActionPerformed
        // TODO add your handling code here:
          if(txteml.getText().trim().length()==0)
               JOptionPane.showMessageDialog(this,"Email Required");
           else if(txtpwd.getPassword().length==0)
               JOptionPane.showMessageDialog(this, "Password Required");
          else
           {
        clsreg obj=new clsreg();
        clsregprp objprp=new clsregprp();
        objprp.setRegeml(txteml.getText());
        String s=new String(txtpwd.getPassword());
        objprp.setRegpwd(s);
        objprp.setRegrol("U");
        objprp.setRegdat(new Date(new java.util.Date().getTime()));
        try {
            obj.insreg(objprp);
            jLabel1.setText("Login");
            jButton1.setVisible(true);
            btnreg.setVisible(false);
            txteml.setText("");
            txtpwd.setText("");
            JOptionPane.showMessageDialog(this, "Registration Successful");
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(this, "Email ID already registered.");
        }
           }
    }//GEN-LAST:event_btnregActionPerformed

