    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           if(txteml.getText().trim().length()==0)
               JOptionPane.showMessageDialog(this,"Email Required");
           else if(txtpwd.getPassword().length==0)
               JOptionPane.showMessageDialog(this, "Password Required");
          else
           {
               String s=new String(txtpwd.getPassword());
               clsregprp p=new clsregprp();
               p.setRegeml(txteml.getText());
               p.setRegpwd(s);
               clsreg obj=new clsreg();
               try {
                   ArrayList arr=obj.logincheck(p);
                   if((int)(arr.get(0))==-1)
                       JOptionPane.showMessageDialog(this, "Email Incorrect");
                   else if((int)(arr.get(0))==-2)
                       JOptionPane.showMessageDialog(this, "Password Incorrect");
                   else
                   {
                       if((char)(arr.get(1))=='A')
                       {
                           admhom obja=new admhom();
                           obja.setVisible(true);
                           setVisible(false);
                       }
                       else if((char)(arr.get(1))=='U')
                       {
                            ucod=(int)(arr.get(0));
                            
                           usrhom obju=new usrhom();
                           obju.setVisible(true);
                           setVisible(false);
                       }
                   }
               } catch (SQLException ex) {
                   Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
               }
               
               
               
           }
    }//GEN-LAST:event_jButton1ActionPerformed

